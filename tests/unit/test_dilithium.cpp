class DilithiumSunnyDayTest
        : public testing::TestWithParam<std::tuple<AsymmetricKey::KeyTypes, std::string>>
{
};

INSTANTIATE_TEST_SUITE_P(
        ValidKeyTypesAndMessages,
        DilithiumSunnyDayTest,
        testing::Values(std::make_tuple(AsymmetricKey::KeyTypes::DILITHIUM2, "Hello World"),
                        std::make_tuple(AsymmetricKey::KeyTypes::DILITHIUM3, "Hello World"),
                        std::make_tuple(AsymmetricKey::KeyTypes::DILITHIUM5, "Hello World"),
                        std::make_tuple(AsymmetricKey::KeyTypes::DILITHIUM2,
                                        "I am exactly of size 32, true!!!"),
                        std::make_tuple(AsymmetricKey::KeyTypes::DILITHIUM3,
                                        "I am exactly of size 32, true!!!"),
                        std::make_tuple(AsymmetricKey::KeyTypes::DILITHIUM5,
                                        "I am exactly of size 32, true!!!")));

void signAndVerify(const DilithiumAsymmetricPrivateKey &privKey, const std::string &messageStr)
{
    std::vector<uint8_t> message(messageStr.begin(), messageStr.end());
    auto ctx = DilithiumSigningCtx(privKey);
    auto res = ctx.signMessage(message);

    auto pub_key_raw = privKey._internal()->getPublicKey();
    auto pub_key = DilithiumAsymmetricPublicKey(std::make_shared<DilithiumKeyImpl>(pub_key_raw));

    auto a = pub_key._internal()->getPublicKey().getKeyData();
    auto b = pub_key_raw.getKeyData();
    // test getter for public Key
    EXPECT_EQ(pub_key._internal()->getPublicKey().getKeyData(), pub_key_raw.getKeyData());

    auto verify_ctx = DilithiumVerificationCtx(pub_key);
    verify_ctx.verifyMessage(res, message);
}

/* Test the KeySpec and the generation of keys that way */
TEST_P(DilithiumSunnyDayTest, ValidKeyTypesAndMessages)
{
    auto spec = DilithiumSpec(std::get<0>(GetParam()));
    auto messageStr = std::get<1>(GetParam());

    auto priv_key = spec.generate();

    auto priv_key_ctx = DilithiumAsymmetricKeypair(priv_key._internal());
    // test getType function
    EXPECT_EQ(priv_key.getType(), std::get<0>(GetParam()));
    signAndVerify(priv_key_ctx, messageStr);
}

